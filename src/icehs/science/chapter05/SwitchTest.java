package icehs.science.chapter05;

public class SwitchTest {

	public static void main(String[] args) {
		
		int a=10;
		switch(a){
		
		case 9: System.out.println("9점입니다."); break;
		case 8: System.out.println("8점입니다."); break;
		case 7: System.out.println("7점입니다."); break;
		case 6: System.out.println("6점입니다."); break;
		case 5: System.out.println("5점입니다."); break;
		case 4: System.out.println("4점입니다."); break;
		case 3: System.out.println("3점입니다."); break;
		case 2: System.out.println("2점입니다."); break;
		case 1: System.out.println("1점입니다."); break;
		default : System.out.println("알 수 없습니다");
	}

  }
}
